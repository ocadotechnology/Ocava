package com.ocadotechnology.scenario;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Denotes a Scenario Test story.  This must be put on each class with a @Test entry point method.  This allows the code
 * to produce a stack trace linking to the step which failed when the test fails to execute correctly.
 */
@Target(value = {ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
public @interface Story {
}
